# c. User Triggered Async Job: Export CSV
# ==========================================
@celery.task()
def export_student_history_csv(student_id, student_email):
    with app.app_context(): # <--- ADDED APPLICATION CONTEXT
        # 1. Fetch data from DB
        apps = db.session.query(Application, PlacementDrive, CompanyProfile)\
            .join(PlacementDrive, Application.drive_id == PlacementDrive.id)\
            .join(CompanyProfile, PlacementDrive.company_id == CompanyProfile.id)\
            .filter(Application.student_id == student_id).all()

        # 2. Generate CSV File
        filename = f"export_{student_id}_{datetime.now().strftime('%Y%m%d%H%M%S')}.csv"
        filepath = os.path.join("static", "exports", filename)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)

        with open(filepath, 'w', newline='') as csvfile:
            writer = csv.writer(csvfile)
            writer.writerow(['Student ID', 'Company Name', 'Drive Title', 'Application Status'])
            
            # ---> FIX: Renamed 'app' to 'student_app' so it doesn't conflict with Flask's 'app'
            for student_app, drive, company in apps:
                writer.writerow([student_id, company.company_name, drive.job_title, student_app.status])

        # 3. Setup Email Data
        download_link = f"http://127.0.0.1:5000/{filepath}"
        subject = "Your Placement Portal CSV Export is Ready"
        body = f"Hello, your application history CSV has been generated. Download it here: {download_link}"

        # 4. Smart Domain Whitelisting Logic
        if student_email.strip().lower().endswith("iitm.ac.in"):
            try:
                msg = Message(subject, recipients=[student_email])
                msg.body = body
                mail.send(msg)
                print(f"[REAL EMAIL SENT] Successfully delivered to official ID: {student_email}")
            except Exception as e:
                print(f"[EMAIL FAILED] Could not send to {student_email}. Error: {str(e)}")
        else:
            print("--------------------------------------------------")
            print(f"[MOCK EMAIL - NON IITM DOMAIN] To: {student_email}")
            print(f"Subject: {subject}")
            print(f"Body: {body}")
            print("--------------------------------------------------")

        return filepath