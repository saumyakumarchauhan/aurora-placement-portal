from datetime import datetime, timedelta
import csv
import os

# =================================================================
# a. Scheduled Job - Daily Reminders (Runs Daily)
# =================================================================
@celery.task()
def daily_deadline_reminder():
    with app.app_context(): # <--- ADDED APPLICATION CONTEXT
        # Find drives closing in exactly 2 days
        target_date = datetime.now().date() + timedelta(days=2)
        closing_drives = PlacementDrive.query.filter_by(application_deadline=target_date, status="Approved").all()
        
        if not closing_drives:
            return "No drives closing soon."

        students = StudentProfile.query.all()
        
        for drive in closing_drives:
            for student in students:
                # Check if student is eligible and HAS NOT applied yet
                has_applied = Application.query.filter_by(student_id=student.id, drive_id=drive.id).first()
                
                if not has_applied and float(student.cgpa) >= float(drive.min_cgpa):
                    user = User.query.get(student.user_id)
                    if user and user.email:
                        subject = f"Action Required: Deadline Approaching for {drive.job_title}"
                        body = f"Hello {student.full_name},\n\nThe application deadline for the '{drive.job_title}' drive is approaching on {target_date}. Since you meet the minimum CGPA criteria, we encourage you to apply before the link closes.\n\nRegards,\nPlacement Cell"
                        
                        try:
                            msg = Message(subject, recipients=[user.email])
                            msg.body = body
                            mail.send(msg)
                            print(f"[REMINDER SENT] -> {user.email}")
                        except Exception as e:
                            print(f"Failed to send reminder to {user.email}: {e}")

        return "Daily reminders process completed."

